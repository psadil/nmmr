#' Convert between angles and radians
#' @aliases rad deg
#'
#' @param radian,degree angles with different units
#' @name angles
NULL

#' @rdname angles
#' @export
deg <- function(radian){
  (radian * 180) / pi
}

#' @rdname angles
#' @export
rad <- function(degree){
  (degree * pi) / 180
}


